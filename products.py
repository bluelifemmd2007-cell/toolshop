def get_product(product_id: int):
    return next((p for p in PRODUCTS if p["id"] == product_id), None)


def get_products(category: str = "all", search: str = "", sort: str = "default"):
    items = list(PRODUCTS)

    if category and category != "all":
        items = [p for p in items if p["category"] == category]

    if search:
        q = search.strip().lower()
        items = [p for p in items if q in p["name"].lower() or q in p["description"].lower()]

    if sort == "price_asc":
        items.sort(key=lambda p: p["price"])
    elif sort == "price_desc":
        items.sort(key=lambda p: p["price"], reverse=True)
    elif sort == "rating":
        items.sort(key=lambda p: p["rating"], reverse=True)

    return items


def category_count(category_id: str) -> int:
    if category_id == "all":
        return len(PRODUCTS)
    return sum(1 for p in PRODUCTS if p["category"] == category_id)
